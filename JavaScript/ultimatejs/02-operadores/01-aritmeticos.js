let a = 5;
let b = 7;

console.log(a + b, 'adicion'); // 12
console.log(a - b, 'resta'); // -2
console.log(a * b, 'multiplicacion'); // 35
console.log(a / b, 'division'); // 0.7142857142857143
console.log(a % b, 'modulo'); // 5
console.log(a ** b, 'potencia'); // 78125

console.log(++a) // primero suma y luego muestra
console.log(a++) // primero muestra y luego suma
console.log(a) // 7

console.log(--a) // primero suma y luego muestra
console.log(a--) // primero muestra y luego suma
console.log(a) // 7